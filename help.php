<p>Mindpad helps you think by letting you just write.</p>
<p>As you type, the text should be formatted automatically and partially processed.</p>
<p>For headings with markers, type in all <span style="color:red; font-size:1.25em">CAPS</span></p>
<p>For bullets, start line with 1 non alphanumeric character like <span style="background-color:yellow">-item</span></p>
<p>Using <code>[sof]</code> and <code>[eof]</code> comment out contained lines</p>
<p>For dividers, have 5 or more nonalphanumerics in a row <span style="text-decoration:underline">======================</span></p>
<p>There are controls down the bottom that let you format text yourself, as well as add links.</p>
<p>Each of the tabs also shows different different data.</p>
<p>Outlines shows markers for each heading that you can navigate with.</p>
<p>Analyze gives you a summary of the text and key concepts in your text.</p>
